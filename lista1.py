#lista de datos (almacenas muchos datos en una variable)
numeros=[1,2,3,4,5]

#acceder a todos los datos de la lista al mismo tiempo
print(numeros)

#acceder a un solo elemento de la lista
print(numeros[0])

#acceder de forma individual a cada elemento de una lista
for numero in numeros:
    print(numero)
    
for numero in numeros:
    print(numero+10)